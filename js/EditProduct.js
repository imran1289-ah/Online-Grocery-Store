function AddItems()
{
  var node = document.createElement("LI");

var textnode = document.createElement("INPUT");
  textnode.setAttribute("type", "text");
  textnode.setAttribute("value", "New Item "); 

  var textnode2 = document.createElement("INPUT");
  textnode2.setAttribute("type", "text");
  textnode2.setAttribute("value", "Amount");
  var textnode3 = document.createElement("INPUT");
  textnode3.setAttribute("type", "text");
  textnode3.setAttribute("value", "Price");
node.appendChild(textnode);
node.appendChild(textnode2);
node.appendChild(textnode3);
document.getElementById("myTable").appendChild(node);
}
function DelItems()
{
  var list = document.getElementById("myTable");
  list.removeChild(list.childNodes[21]);; 
}

